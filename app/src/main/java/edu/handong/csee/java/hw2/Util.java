package edu.handong.csee.java.hw2;

/**
 * This is a class that changes stringValue to upper case.
 */
public class Util {

    /**
     * This is a method that changes stringValue to upper case.
     * @param stringValue
     * @return String
     */
    static String getUppercaseString(String stringValue) {
        return stringValue.toUpperCase();
    }
    
}
